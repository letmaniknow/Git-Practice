package com.mmva.newsapp.domain.newsletter.enums.core;

/**
 * Newsletter campaign type enumeration.
 *
 * <p>
 * Defines the different types of newsletter campaigns for targeting and
 * analytics.
 * </p>
 *
 * @author MMVA Team
 * @since 1.0.0
 */
public enum NewsletterCampaignType {

    /**
     * Regular newsletter - standard content distribution.
     */
    REGULAR,

    /**
     * Welcome series - onboarding new subscribers.
     */
    WELCOME,

    /**
     * Re-engagement campaign - win back inactive subscribers.
     */
    REENGAGEMENT,

    /**
     * Promotional campaign - marketing and special offers.
     */
    PROMOTIONAL
}